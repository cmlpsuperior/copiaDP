package models;

public class PersonaReniec {
	private String nombre;
	private String apellidos;
	private int dni;
	private int ubigeo;
	private int idHuella;
	private int idFirma;
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellidos() {
		return apellidos;
	}
	public void setApellidos(String apellidos) {
		this.apellidos = apellidos;
	}
	public int getDni() {
		return dni;
	}
	public void setDni(int dni) {
		this.dni = dni;
	}
	public int getUbigeo() {
		return ubigeo;
	}
	public void setUbigeo(int ubigeo) {
		this.ubigeo = ubigeo;
	}
	public int getIdHuella() {
		return idHuella;
	}
	public void setIdHuella(int idHuella) {
		this.idHuella = idHuella;
	}
	public int getIdFirma() {
		return idFirma;
	}
	public void setIdFirma(int idFirma) {
		this.idFirma = idFirma;
	}
	
	
}
